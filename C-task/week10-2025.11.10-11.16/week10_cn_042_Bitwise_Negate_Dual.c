#include<stdio.h>
int main(){
    unsigned a,b,c,d;
    printf("请输入整数:\n");
    scanf("%o",&a);
    b=a>>4;
    c=~(~0<<4);
    d=b&c;
    printf("%o\n%o\n",a,d);
    return 0;
}
// | 步骤 | 表达式          | 二进制（每 4 位一组，共 8 组）                        | 十六进制         | 十进制                      |
// | -- | ------------ | ----------------------------------------- | ------------ | ------------------------ |
// | ①  | `0`          | `0000 0000 0000 0000 0000 0000 0000 0000` | `0x00000000` | 0                        |
// | ②  | `~0`         | `1111 1111 1111 1111 1111 1111 1111 1111` | `0xFFFFFFFF` | −1（有符号）或 4294967295（无符号） |
// | ③  | `~0 << 4`    | `1111 1111 1111 1111 1111 1111 1111 0000` | `0xFFFFFFF0` | 低 4 位变 0                 |
// | ④  | `~(~0 << 4)` | `0000 0000 0000 0000 0000 0000 0000 1111` | `0x0000000F` | 15                       |
// 第 ③ 步把最低 4 位清成 0；
// 第 ④ 步再整体取反，于是只有最低 4 位变成 1，其余全 0


#include<stdio.h>
int main(){
    int a,b;
    a=234;
    b=~a;
    printf("a的按位取反值为(十进制)%d\n",b);
    a=~a;
    printf("a的按位取反值为(十六进制)%x\n",a);
    return 0;
}